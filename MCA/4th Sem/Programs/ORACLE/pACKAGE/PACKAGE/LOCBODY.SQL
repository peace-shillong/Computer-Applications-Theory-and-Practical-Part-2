CREATE OR REPLACE PACKAGE BODY LOC IS 
  N NUMBER;

  PROCEDURE TO_INSERT(ILOCID NUMBER,ICITY VARCHAR2) IS
  BEGIN
    INSERT INTO locations VALUES(ILOCID,ICITY);
    COMMIT;
  END TO_INSERT;

  PROCEDURE TO_UPDATE(ULOCID NUMBER, UCITY VARCHAR2) IS
  BEGIN
    UPDATE locations
    SET city=UCITY
    WHERE locationId=ULOCID;
    COMMIT;
  END TO_UPDATE;

  PROCEDURE TO_DELETE(DLOCID NUMBER) IS
  BEGIN
    DELETE FROM locations
    WHERE locationId=DLOCID;
    COMMIT;
  END TO_DELETE;

  FUNCTION F1 RETURN NUMBER IS
  BEGIN
    SELECT COUNT(locationId)INTO N
    FROM locations;
    RETURN(N);
  END F1;
END LOC;
    
    